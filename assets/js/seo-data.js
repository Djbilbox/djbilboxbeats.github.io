/* Helper: get current language */
window.getCurrentLang = () => {
  const storedLang = localStorage.getItem('djbilbox_lang');
  if (storedLang && window.TRANSLATIONS[storedLang]) return storedLang;

  const navLang = navigator.language.split('-')[0];
  if (window.TRANSLATIONS[navLang]) return navLang;

  return 'en';
};

/* Helper: translate a key */
window.t = (key) => {
  const lang = window.getCurrentLang();
  return window.TRANSLATIONS[lang]?.[key] || window.TRANSLATIONS['en']?.[key] || key;
};

/* Set language */
window.setLanguage = (lang) => {
  if (window.TRANSLATIONS[lang]) {
    localStorage.setItem('djbilbox_lang', lang);
    location.reload();
  }
};
